const makeTable = (headers: string[], rows: string[][], caption: string) => {
  const headerRow = new TableRow({
    tableHeader: true,
    children: headers.map((h) =>
      new TableCell({
        shading: { fill: "F0EBE0" },
        borders: {
          top: { style: BorderStyle.SINGLE, size: 4, color: "888888" },
          bottom: { style: BorderStyle.SINGLE, size: 4, color: "888888" },
          left: { style: BorderStyle.SINGLE, size: 4, color: "888888" },
          right: { style: BorderStyle.SINGLE, size: 4, color: "888888" },
        },
        children: [
          new Paragraph({
            alignment: AlignmentType.CENTER,
            children: [new TextRun({ text: h, bold: true, size: 24, font: FONT_SANS })],
          }),
        ],
      })
    ),
  });

  const dataRows = rows.map((row, ri) =>
    new TableRow({
      children: row.map((cell) =>
        new TableCell({
          shading: ri % 2 === 1 ? { fill: "FAF7F0" } : undefined,
          borders: {
            top: { style: BorderStyle.SINGLE, size: 2, color: "BBBBBB" },
            bottom: { style: BorderStyle.SINGLE, size: 2, color: "BBBBBB" },
            left: { style: BorderStyle.SINGLE, size: 2, color: "BBBBBB" },
            right: { style: BorderStyle.SINGLE, size: 2, color: "BBBBBB" },
          },
          children: [
            new Paragraph({
              children: [new TextRun({ text: cell, size: 24, font: FONT_SANS })],
            }),
          ],
        })
      ),
    })
  );

  return [
    new Paragraph({
      spacing: { before: 200, after: 100 },
      children: [new TextRun({ text: caption, italic: true, size: 24, font: FONT_SANS })],
    }),
    new Table({
      width: { size: 100, type: WidthType.PERCENTAGE },
      rows: [headerRow, ...dataRows],
    }),
    emptyLine(),
  ];
};

