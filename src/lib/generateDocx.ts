const para = (text: string, opts?: {
  bold?: boolean;
  italic?: boolean;
  size?: number;
  center?: boolean;
  indent?: boolean;
  spaceBefore?: number;
  spaceAfter?: number;
  heading?: typeof HeadingLevel[keyof typeof HeadingLevel];
}) => new Paragraph({
  heading: opts?.heading,
  alignment: opts?.center ? AlignmentType.CENTER : (opts?.indent !== false ? AlignmentType.BOTH : AlignmentType.LEFT),
  spacing: { before: (opts?.spaceBefore ?? 0) * 20, after: (opts?.spaceAfter ?? 120) },
  indent: opts?.indent !== false && !opts?.center && !opts?.heading ? { firstLine: 720 } : undefined,
  children: [
    new TextRun({
      text,
      bold: opts?.bold,
      italic: opts?.italic,
      size: (opts?.size ?? 14) * 2,
      font: FONT_SERIF,
    }),
  ],
});

